#include "3-calc.h"

/**
 * op_add - adds two intgers
 * @a: first int
 * @b: second int
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two intgers
 * @a: first int
 * @b: second int
 *
 * Return: the diff
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication two intgers
 * @a: first int
 * @b: second int
 *
 * Return: the multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two intgers
 * @a: first int
 * @b: second int
 *
 * Return: the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module two intgers
 * @a: first int
 * @b: second int
 *
 * Return: the reaminder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
